    private void itemListarAlumnosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemListarAlumnosActionPerformed
        try {
            List<Alumno> listaAlumnos = alumnoController.getAllAlumnos();

            if (listaAlumnos == null || listaAlumnos.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No hay alumnos registrados", "Resultado negativo", JOptionPane.WARNING_MESSAGE);
                return;
            }

            VentanaListarAlumnos ventana = new VentanaListarAlumnos(this, true, alumnoController, cursoController);
            ventana.setLocationRelativeTo(this);
            ventana.setVisible(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al intentar abrir la lista de alumnos: " + e.getMessage());
            e.printStackTrace();
        }

    }//GEN-LAST:event_itemListarAlumnosActionPerformed

